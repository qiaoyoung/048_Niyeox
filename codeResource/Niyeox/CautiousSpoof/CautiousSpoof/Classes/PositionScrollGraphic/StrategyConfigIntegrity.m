Byte * StrategyDataToCache(Byte *data) {
    int normEntry = data[0];
    int flowDigitalMount = data[1];
    Byte gravity = data[2];
    int plotTide = data[3];
    if (!normEntry) return data + plotTide;
    for (int i = plotTide; i < plotTide + flowDigitalMount; i++) {
        int value = data[i] + gravity;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[plotTide + flowDigitalMount] = 0;
    return data + plotTide;
}

NSString *StringFromStrategyData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)StrategyDataToCache(data)];
}
