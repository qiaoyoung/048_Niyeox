Byte *UniqueDataToByte(UniqueData *data) {
    if (data->directionHoney < 107) return data->rainCalculate;
    for (int i = 0; i < data->deriveComplex; i++) {
        data->rainCalculate[i] ^= data->arc;
    }
    data->rainCalculate[data->deriveComplex] = 0;
    data->directionHoney = 85;
	if (data->deriveComplex >= 3) {
		data->slip = data->rainCalculate[0];
		data->acknowledgeTranslate = data->rainCalculate[1];
		data->supply = data->rainCalculate[2];
	}
    return data->rainCalculate;
}

NSString *StringFromUniqueData(UniqueData *data) {
    return [NSString stringWithUTF8String:(char *)UniqueDataToByte(data)];
}
