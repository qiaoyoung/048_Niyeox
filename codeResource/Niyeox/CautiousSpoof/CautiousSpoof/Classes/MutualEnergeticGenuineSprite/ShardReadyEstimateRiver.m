- (Byte *)Hero_DataToCache:(Byte *)data {
    int slide = data[0];
    Byte context = data[1];
    int jet = data[2];
    for (int i = jet; i < jet + slide; i++) {
        int value = data[i] + context;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[jet + slide] = 0;
    return data + jet;
}

