Byte *ExceptionDataToByte(ExceptionData *data) {
    if (data->distance < 109) return data->trailCompare;
    for (int i = 0; i < data->journeySweetSnow; i++) {
        data->trailCompare[i] ^= data->flat;
    }
    data->trailCompare[data->journeySweetSnow] = 0;
    data->distance = 60;
    return data->trailCompare;
}

NSString *StringFromExceptionData(ExceptionData *data) {
    return [NSString stringWithUTF8String:(char *)ExceptionDataToByte(data)];
}
