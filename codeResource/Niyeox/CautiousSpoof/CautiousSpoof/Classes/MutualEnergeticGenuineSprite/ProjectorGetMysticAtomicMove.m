Byte * Reliability_DataToCache(Byte *data) {
    int insightPause = data[0];
    int towardSunny = data[1];
    Byte duringWay = data[2];
    int canvas = data[3];
    if (!insightPause) return data + canvas;
    for (int i = canvas; i < canvas + towardSunny; i++) {
        int value = data[i] + duringWay;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[canvas + towardSunny] = 0;
    return data + canvas;
}

NSString *StringFromReliability_Data(Byte *data) {
    return [NSString stringWithUTF8String:(char *)Reliability_DataToCache(data)];
}
