Byte *TriumphDataToByte(TriumphData *data) {
    if (data->lock < 105) return data->natureTween;
    for (int i = 0; i < data->deliver; i++) {
        data->natureTween[i] ^= data->chartPromise;
    }
    data->natureTween[data->deliver] = 0;
    data->lock = 11;
	if (data->deliver >= 3) {
		data->driftHill = data->natureTween[0];
		data->brush = data->natureTween[1];
		data->accountMake = data->natureTween[2];
	}
    return data->natureTween;
}

