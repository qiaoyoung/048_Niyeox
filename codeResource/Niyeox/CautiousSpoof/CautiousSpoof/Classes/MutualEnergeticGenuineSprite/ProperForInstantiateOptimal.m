Byte * Trend_DataToCache(Byte *data) {
    int within = data[0];
    int strike = data[1];
    Byte signaturePleased = data[2];
    int cliff = data[3];
    if (!within) return data + cliff;
    for (int i = cliff; i < cliff + strike; i++) {
        int value = data[i] + signaturePleased;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[cliff + strike] = 0;
    return data + cliff;
}

NSString *StringFromTrend_Data(Byte *data) {
    return [NSString stringWithUTF8String:(char *)Trend_DataToCache(data)];
}
