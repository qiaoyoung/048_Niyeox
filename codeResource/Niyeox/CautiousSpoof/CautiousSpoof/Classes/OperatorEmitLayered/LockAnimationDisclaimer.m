Byte * PreciousDataToCache(Byte *data) {
    int rock = data[0];
    int insightMark = data[1];
    Byte rich = data[2];
    int rainDimensionView = data[3];
    if (!rock) return data + rainDimensionView;
    for (int i = rainDimensionView; i < rainDimensionView + insightMark; i++) {
        int value = data[i] + rich;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[rainDimensionView + insightMark] = 0;
    return data + rainDimensionView;
}

NSString *StringFromPreciousData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)PreciousDataToCache(data)];
}
